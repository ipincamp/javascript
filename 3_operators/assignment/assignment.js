/**
 * Assignment Operator / Operator Penugasan
 * 
 * =	    x = y	    x = y
 * +=	    x += y	    x = x + y
 * -=	    x -= y	    x = x - y
 * *=	    x *= y	    x = x * y
 * /=	    x /= y	    x = x / y
 * %=	    x %= y	    x = x % y
 * <<=	    x <<= y	    x = x << y
 * >>=	    x >>= y	    x = x >> y
 * >>>=	    x >>>= y	x = x >>> y
 * &=	    x &= y	    x = x & y
 * ^=	    x ^= y	    x = x ^ y
 * |=	    x |= y	    x = x | y
 * **=	    x **= y	    x = x ** y
 */

let tambah = 100;
tambah += 10;
console.log(tambah);

let kurang = 100;
kurang -= 5;
console.log(kurang);

let kali = 100;
kali *= 5;
console.log(kali);

let bagi = 100;
bagi /= 5;
console.log(bagi);
